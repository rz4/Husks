def test_husks_init_run_stub_command():
    """Generated project succeeds with `husks run --stub`."""
    tmpdir = tempfile.mkdtemp(prefix="init-run-")
    try:
        # Run husks init
        run_husks_cli("init", str(tmpdir))

        # Create site directory
        site = Path(tmpdir) / "site"
        site.mkdir()

        # Run husks run --stub (init creates core-bootstrap.locke)
        design_path = Path(tmpdir) / "core-bootstrap.locke"
        result = run_husks_cli(
            "run", str(design_path), "--stub", "--site", str(site), cwd=str(tmpdir)
        )

        # Should succeed (exit 0 = committed)
        assert result.returncode == 0, (
            f"husks run --stub should succeed on generated project\n"
            f"stdout: {result.stdout}\nstderr: {result.stderr}"
        )

        # Should show sealed status in DAG view
        assert "sealed" in result.stdout.lower()

    finally:
        shutil.rmtree(tmpdir, ignore_errors=True)


