def test_husks_init_creates_valid_project():
    """husks init creates a project that passes check and run --stub."""
    tmpdir = tempfile.mkdtemp(prefix="init-test-")
    try:
        # Run husks init in temp directory
        result = run_husks_cli("init", str(tmpdir))

        # Should succeed
        assert result.returncode == 0, (
            f"husks init should succeed, got {result.returncode}\n"
            f"stdout: {result.stdout}\nstderr: {result.stderr}"
        )

        # Should create design.json
        design_path = Path(tmpdir) / "design.json"
        assert design_path.exists(), "design.json should be created"

        # design.json should be valid JSON
        with open(design_path) as f:
            design = json.load(f)

        # Should pass validation
        errors = check(design)
        assert len(errors) == 0, f"Generated design should be valid, got: {errors}"

        # Should have expected structure
        assert design["name"] == "demo"
        assert design["fuel"] > 0
        assert "target" in design
        assert len(design["rules"]) > 0

    finally:
        shutil.rmtree(tmpdir, ignore_errors=True)


